/**
 * Attempts to translate a dynamic string from English to Portuguese
 * using a dictionary of known church terms.
 * Returns the original string if language is 'en' or no match found.
 */
export function translateDynamicText(text: string | undefined, language: string): string {
    if (!text || language !== 'pt') return text || "";

    // 1. Check for exact match first
    if (CHURCH_TERMS[text]) {
        return CHURCH_TERMS[text];
    }

    // 2. Perform word-by-word / phrase substitution
    let translated = text;

    // Sort terms by length (descending) to match longest phrases first
    // e.g. match "Youth Service" before "Service"
    const terms = Object.keys(CHURCH_TERMS).sort((a, b) => b.length - a.length);

    for (const term of terms) {
        // Create a regex to match the term appearing as a whole word or significant part
        // We use 'gi' for global case-insensitive
        const regex = new RegExp(`\\b${term}\\b`, 'gi');
        if (regex.test(translated)) {
            translated = translated.replace(regex, CHURCH_TERMS[term]);
        }
    }

    return translated;
}
